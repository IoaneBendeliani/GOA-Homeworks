#გააკეთეთ 5-5 მაგალითი თთოეულ შედარების ოპერატორზე (>, >=, <, <=, ==, !=), გვერდზე კომენტარის საშვალებით მიუთითეთ თუ რომელ boolean მნიშვნელობას გამოიტანას, აიღეთ მრავალფეროვანი კომბინაციები, შეეცადეთ გქონდეთ ყველა ვარიანტი


print(10 > 5)   # True
print(3 > 8)    # False
print(-2 > -5)  # True
print(7 > 7)    # False
print(0 > -1)   # True

print(10 >= 5)   # True
print(3 >= 8)    # False
print(-2 >= -5)  # True
print(7 >= 7)    # True
print(0 >= 1)    # False

print(10 < 5)   # False
print(3 < 8)    # True
print(-2 < -5)  # False
print(7 < 7)    # False
print(0 < 1)    # True

print(10 <= 5)   # False
print(3 <= 8)    # True
print(-2 <= -5)  # False
print(7 <= 7)    # True
print(0 <= 1)    # True

print(10 == 5)   # False
print(3 == 8)    # False
print(-2 == -2)  # True
print(7 == 7)    # True
print(0 == 1)    # False

print(10 != 5)   # True
print(3 != 8)    # True
print(-2 != -2)  # False
print(7 != 7)    # False
print(0 != 1)    # True