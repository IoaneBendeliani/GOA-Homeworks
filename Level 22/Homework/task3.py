#ახსენით რას ნიშნავს რომ list და string არის დალაგებული მიმდევრობები, კომენტარებით დაწერეთ მათ თვისებებს შორის განსხვავებები. მოიყვანეთ მაგალითები

#როდესაც ვამბობთ, რომ list და string არის დალაგებული მიმდევრობები, ვგულისხმობთ, რომ მათში ელემენტებს აქვთ ზუსტი თანმიმდევრობა, ანუ თითოეულ ელემენტს თავისი ინდექსი გააჩნია — პირველი ელემენტი 0-იან ინდექსზეა, მეორე — 1-ზე და ასე შემდეგ.

# List არის ელემენტების კოლექცია, სადაც ელემენტებად შეიძლება იყოს ნებისმიერი ტიპის მონაცემი
my_list = [1, "apple", 3.14, True]

# String არის მხოლოდ სიმბოლოების (character-ების) მიმდევრობა
my_string = "apple"

# ორივეს აქვს ინდექსები
print(my_list[1])    # "apple"
print(my_string[1])  # 'p'

#  string უცვლადია (immutable) — მისი ელემენტების შეცვლა პირდაპირ არ შეიძლება
# my_string[0] = 'A'  # ეს გამოიწვევს შეცდომას

# list კი შეცვლადია (mutable) — შეგვიძლია ინდექსით ელემენტის შეცვლა
my_list[0] = 100
print(my_list)  # [100, 'apple', 3.14, True]

#ორივე შეიძლება გამოყენებული იყოს ციკლში
for char in my_string:
    print(char)

for item in my_list:
    print(item)

# ორივეს შეგვიძლია დავამატოთ ელემენტები, მაგრამ განსხვავებულად
# string – მხოლოდ კონკატენაცია (შეერთება)
new_string = my_string + " pie"
print(new_string)  # "apple pie"

# list – append() მეთოდით ან სხვა სიებთან გაერთიანებით
my_list.append("new")
print(my_list)  # [100, 'apple', 3.14, True, 'new']